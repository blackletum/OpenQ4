#!/usr/bin/env python3
"""Compile GLSL shaders to SPIR-V and emit an embedded-bytes C header.

The renderer-vk module embeds its SPIR-V so no runtime staging or pak
coupling exists (assetless-startup safe). The generated header is COMMITTED;
builds never require glslang. When glslangValidator is available,
tools/tests/vk_shader_header_pin.py regenerates and byte-compares so drift
between the .vert/.frag sources and the committed header fails loudly.
"""

from __future__ import annotations

import argparse
import pathlib
import shutil
import subprocess
import sys
import tempfile


def find_glslang(explicit: str | None) -> str | None:
    if explicit:
        return explicit if pathlib.Path(explicit).is_file() else None
    tool = shutil.which("glslangValidator")
    if tool:
        return tool
    import os

    sdk = os.environ.get("VULKAN_SDK")
    if sdk:
        for name in ("glslangValidator.exe", "glslangValidator"):
            candidate = pathlib.Path(sdk) / "Bin" / name
            if candidate.is_file():
                return str(candidate)
    return None


def compile_spirv(glslang: str, source: pathlib.Path, out_dir: pathlib.Path) -> bytes:
    spv_path = out_dir / (source.name + ".spv")
    result = subprocess.run(
        [glslang, "-V", str(source), "-o", str(spv_path)],
        capture_output=True,
        text=True,
    )
    if result.returncode != 0:
        raise RuntimeError(
            f"glslangValidator failed for {source}:\n{result.stdout}\n{result.stderr}"
        )
    return spv_path.read_bytes()


def emit_array(name: str, data: bytes) -> str:
    lines = [f"static const unsigned char {name}[] = {{"]
    for i in range(0, len(data), 16):
        chunk = ", ".join(f"0x{b:02x}" for b in data[i : i + 16])
        lines.append(f"\t{chunk},")
    lines.append("};")
    lines.append(f"static const unsigned int {name}_size = {len(data)}u;")
    return "\n".join(lines)


def main(argv: list[str]) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--glslang", help="Explicit glslangValidator path.")
    parser.add_argument("--header-out", required=True)
    parser.add_argument(
        "shaders",
        nargs="+",
        help="GLSL shader files; array names derive from file names (gui.vert -> vk_gui_vert_spv).",
    )
    args = parser.parse_args(argv)

    glslang = find_glslang(args.glslang)
    if glslang is None:
        print(
            "glslangValidator not found (PATH or VULKAN_SDK); cannot regenerate the shader header.",
            file=sys.stderr,
        )
        return 1

    blocks = [
        "// GENERATED by tools/build/spirv_to_header.py — do not edit by hand.",
        "// Regenerate after editing src/renderer/Vulkan/shaders/*;",
        "// tools/tests/vk_shader_header_pin.py guards drift.",
        "#ifndef __VK_GUI_SHADERS_SPV_H__",
        "#define __VK_GUI_SHADERS_SPV_H__",
        "",
    ]
    with tempfile.TemporaryDirectory() as tmp:
        tmp_dir = pathlib.Path(tmp)
        for shader in args.shaders:
            source = pathlib.Path(shader)
            data = compile_spirv(glslang, source, tmp_dir)
            array_name = "vk_" + source.name.replace(".", "_") + "_spv"
            blocks.append(emit_array(array_name, data))
            blocks.append("")
    blocks.append("#endif")
    blocks.append("")

    out_path = pathlib.Path(args.header_out)
    out_path.write_text("\n".join(blocks), encoding="utf-8", newline="\n")
    print(f"wrote {out_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
